// Format currency based on language (ES/DE/FR/AR use European format, EN uses US format)
export function formatCurrency(amount: number, lang: Language, showDecimals = true): string {
  const isEnglish = lang === 'en';
  
  if (showDecimals) {
    if (isEnglish) {
      // English format: €5,420.20
      return `€${amount.toLocaleString('en-US', { minimumFractionDigits: 2, maximumFractionDigits: 2 })}`;
    } else {
      // European format: 5.420,20 €
      return `${amount.toLocaleString('de-DE', { minimumFractionDigits: 2, maximumFractionDigits: 2 })} €`;
    }
  } else {
    if (isEnglish) {
      return `€${amount.toLocaleString('en-US')}`;
    } else {
      return `${amount.toLocaleString('de-DE')} €`;
    }
  }
}

// Format number based on language
export function formatNumber(num: number, lang: Language): string {
  const isEnglish = lang === 'en';
  return num.toLocaleString(isEnglish ? 'en-US' : 'de-DE');
}